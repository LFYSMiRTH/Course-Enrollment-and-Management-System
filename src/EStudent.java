    private void loadStudentData(){
        DefaultTableModel model = (DefaultTableModel) enrolledTable.getModel();
        model.setRowCount(0);
        
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mrjtl_db", "root", "");
            String sql = "SELECT name, course, year_level FROM s_db"; 
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()) {
                String name = rs.getString("name");
                String course = rs.getString("course");
                String yearLevel = rs.getString("year_level");
                
                model.addRow(new Object[]{name, course, yearLevel});
            }
            
            rs.close();
            pst.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
